#class definition
class calculator:
    def add(x,y):
        sum = x+y
        print (sum)
    def subtract(x,y):
        diff = x-y
        print (diff)
    def multiply(x,y):
        product=x*y
        print (product)
    def divide(x,y):
        div=x/y
        print (div)
# call methods of the class
calculator.add(5,3)
calculator.subtract(5,3)
calculator.multiply(5,3)
calculator.divide(5,3)
